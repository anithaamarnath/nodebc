let mutants = ['Professor X',
    'Cyclops',
    'Iceman',
    'Angel',
    'Beast',
    'Phoenix'
]


console.log('This is a sorted Item', mutants.sort());//[ 'Angel', 'Beast', 'Cyclops', 'Iceman', 'Phoenix', 'Professor X' ]
console.log('This is a sorted Item', mutants.reverse())//[ 'Professor X', 'Phoenix', 'Iceman', 'Cyclops', 'Beast', 'Angel' ]
console.log('This is a sorted Item', mutants.join(' * '))//Professor X * Phoenix * Iceman * Cyclops * Beast * Angel