let firstName = 'Anitha'
let lastName =  'Amaranth';
let firstNameLength = firstName.length;
let lastNameLength = lastName.length;
let charsDiff = firstNameLength - lastNameLength ;
let charsLength = firstNameLength > lastNameLength ;

console.log(`My firstName is  ${firstName} and it is ${firstNameLength } characters long`);
console.log(`My lastName is ${lastName} and it is ${lastNameLength} characters long`);
console.log(`The character difference between my first name and last name is ${charsDiff }`);
console.log(`My first name is longer than my last name: ${charsLength }`)


